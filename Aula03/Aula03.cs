class Aula03
{
    static void Main()
    {
        // Literal char
        Console.WriteLine('b'); // Com aspas simples
        // Literal int 
        Console.WriteLine(123);
        // Literal decimal
        Console.WriteLine(12.3m); // Pode usar a letra m maiúscula ou minúscula
        Console.WriteLine(12.3M);
        // Literal bool
        Console.WriteLine(true);
        Console.WriteLine(false);
    }
}