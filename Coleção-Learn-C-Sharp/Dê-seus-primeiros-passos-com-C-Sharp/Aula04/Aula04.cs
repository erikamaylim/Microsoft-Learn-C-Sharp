// Declarar variáveis
class Aula04
{
    static void Main()
    {        
        // Os nomes de variáveis devem ser camelCase. Exemplo: primeiroNome
        string primeiroNome;          // declarar variável
        primeiroNome = "Suki";       // atribuir valor à variável 
        Console.WriteLine(primeiroNome);
        string sobrenome = "Pantini";     // inicializar variável. Quando a variável é declarada e recebe valor na mesma linha
        Console.WriteLine(sobrenome);
    }
}